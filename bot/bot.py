def convert_to_dialogue(messages):
    dialogue = ''
    for message in messages:
        if 'user' in message:
            user_message = message['user']
            dialogue += f"–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å: {user_message}\n"
        if 'bot' in message:
            bot_message = message['bot']
            dialogue += f"–ë–æ—Ç: {bot_message}\n"
    return dialogue

def is_before_today(dt):
    if dt is None:
        return True
    today = date.today()
    return dt.date() < today

